#!/usr/bin/env node
import assert from "node:assert/strict";
import {readFileSync} from "node:fs";
import {resolve} from "node:path";
const root=resolve(process.argv[2]||".");
const read=rel=>readFileSync(resolve(root,rel),"utf8");
const familyRoot="internal/household/family";
const store=read(`${familyRoot}/store.go`);
const summary=read(`${familyRoot}/summary.go`);
const sessions=read(`${familyRoot}/sessions.go`);
const inboxViews=read(`${familyRoot}/inbox.go`);
const pins=read(`${familyRoot}/pins.go`);
const privateStore=read(`${familyRoot}/service.go`);
const facade=read("cmd/dashboard-control-server/family_board_facade.go");
const inbox=read("cmd/dashboard-control-server/family_board_inbox_http.go");
const inboxActions=read(`${familyRoot}/inbox_actions.go`);
const httpServer=read("cmd/dashboard-control-server/http_server.go");
const board=read("ui/family-board.js");
const people=read("ui/js/control-people.js");
const peopleHTTP=read("cmd/dashboard-control-server/household_people_inbox_pins.go");
const backup=read("cmd/dashboard-control-server/config_backups.go");
const restore=read("cmd/dashboard-control-server/config_backup_restore.go");
assert.match(store,/func Scope\(/,"Family Board messages need explicit household/direct scope");
assert.match(summary,/func PublicPayload\(/,"public Board response must filter direct messages");
assert.match(summary,/Stamp\(note\["householdAcknowledgedAt"\]\)/,"only unacknowledged household urgent messages may alert");
assert.match(inboxViews,/InboxVisibleLimit/,"private inbox rendering must be bounded");
assert.match(inboxViews,/recipientPersonId/,"direct messages need an explicit recipient");
assert.match(inboxViews,/recipientDeletedAt/,"recipient cleanup must remain scoped to the personal inbox");
assert.match(inboxViews,/case "archive"/,"private inbox views need an archive lane");
assert.match(inboxViews,/"archive":\s*archive/,"inbox payload must return a per-inbox archive collection");
assert.match(inbox,/X-DashGo-Inbox-Token/,"private inbox endpoints require an inbox-scoped token");
assert.match(sessions,/InboxSessionTTL/,"private inbox sessions must be bounded in the Family Board service");
assert.match(inbox,/ReadInbox\(personID\)/,"route adapter must delegate inbox-read mutation to the child service");
assert.match(inbox,/SendDirect\(senderID, body\)/,"route adapter must delegate direct-send mutation to the child service");
assert.match(inbox,/MutateDirect\(entry\.PersonID, messageID, action\)/,"route adapter must delegate direct-message correction to the child service");
assert.match(inboxActions,/only the recipient can archive/,"recipient correction path must be explicit");
assert.match(inboxActions,/only the recipient can restore/,"recipient archive correction path must include restore");
assert.match(inboxActions,/only the recipient can delete this archived message/,"recipient deletion must stay inbox scoped");
assert.match(inboxActions,/archive this private message before deleting it/,"active inbox messages must be archived before destructive deletion");
assert.match(inboxActions,/action != "archive" && action != "restore" && action != "delete"/,"private message actions must whitelist archive, restore, and delete");
assert.match(inboxActions,/a read message cannot be withdrawn/,"senders cannot silently withdraw read messages");
assert.match(pins,/NewPINPayload/,"personal inbox PINs must be server-side verifiers");
assert.match(pins,/0600/,"personal inbox verifier must be owner-only");
assert.match(privateStore,/\.dashboard-family-board\.json/,"direct message storage must live outside the dashboard static tree");
assert.match(privateStore,/fileio\.WriteAtomic\(s\.storePath, data, 0600\)/,"private message storage must be owner-only");
assert.match(facade,/internal\/household\/family/,"core must delegate Family Board ownership to the bounded child package");
assert.doesNotMatch(facade,/\bfamilyBoardMu\b|\binboxSessions\b|\binboxPinFailures\b/,"core facade must not recreate Family Board state ownership");
assert.doesNotMatch(store,/cmd\/dashboard-control-server/,"Family Board service must not import core runtime source");
assert.match(httpServer,/staticPrivatePath\(rel\)/,"legacy browser-addressable Board storage must be denied");
assert.doesNotMatch(pins,/"pin"\s*:/,"PIN plaintext must not be persisted");
assert.match(board,/\["board","Household Board"\],\["inboxes","Inboxes"\]/,"Family Board must contain public board and private inbox views");
assert.match(board,/Private messages never create a dashboard alert/,"private urgent behavior must be stated in compose UI");
assert.match(board,/X-DashGo-Inbox-Token/,"browser must present inbox token only for the selected private session");
assert.match(board,/releaseInbox\(/,"close/lock must revoke local private session state");
assert.match(board,/\["archive","Archive"/,"each unlocked personal inbox must expose its own Archive pane");
assert.match(board,/renderInboxConfirm\(/,"private deletion must use a themed in-overlay confirmation");
assert.match(board,/Delete from inbox/,"archive deletion must be explicit about its recipient-only scope");
assert.match(board,/sender’s Sent history stays unchanged/,"private archive UI must explain that recipient deletion does not erase sender history");
assert.doesNotMatch(board,/\b(?:window\.)?(?:alert|confirm|prompt)\s*\(/,"Family Board must retain themed in-overlay confirmations");
assert.match(people,/This PIN protects this inbox on the shared display/,"People must explain optional inbox PIN behavior");
assert.match(people,/Remove inbox PIN/,"People must support forgotten-PIN recovery");
assert.doesNotMatch(people,/Personal inbox PINs need Dashboard Control security/,"People must not show a master-PIN prerequisite");
assert.doesNotMatch(peopleHTTP,/lockConfig\(\)\["enabled"\]/,"personal PIN mutations must not require master Dashboard Control protection");
assert.match(peopleHTTP,/normal\n\/\/ request\/session guard/,"personal PIN mutations must retain the normal Dashboard Control request guard");
assert.match(backup,/secrets\/family-board\.json/,"owner-only backup must include private Family Message Board data");
assert.match(backup,/secrets\/family-board-inbox-pins\.json/,"owner-only backup must include personal inbox verifier");
assert.match(restore,/restoreFamilyBoardInboxPins/,"restore must explicitly handle the private verifier");
console.log("PASS: Family Board direct inboxes are local, bounded, individually protected, recipient-archivable/deletable, and do not leak urgent private messages into dashboard alerts");
