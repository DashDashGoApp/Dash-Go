import assert from "node:assert/strict";
import fs from "node:fs";
import path from "node:path";
import {fileURLToPath} from "node:url";

const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),"..");
const read=rel=>fs.readFileSync(path.join(root,rel),"utf8");
const lists=read("ui/lists-core.js")+"\n"+read("ui/lists-actions.js");
const grocery=read("ui/lists-grocery.js");
const css=read("ui/lists.css");
const launcher=read("ui/js/app-launcher.js");
const http=read("cmd/dashboard-control-server/todo_grocery_memory_http.go");
const memory=read("internal/todo/todo_grocery_memory.go");
const settingsService=read("internal/settings/service.go");

assert.match(lists,/groceryManage:false/,"Lists state must keep an explicit Quick add manager view");
assert.match(lists,/DashGoGroceryQuickAdd\?\.renderQuickAdd/,"normal Grocery view must delegate Quick add rendering lazily");
assert.match(lists,/manager\.renderManager/,"Grocery manager must use the canonical Lists render lifecycle");
assert.match(lists,/edit\.textContent="Edit"/,"active Grocery tasks need a visible Edit action");
assert.match(lists,/del\.textContent="Remove"/,"active Grocery tasks need a visible Remove action");
assert.match(grocery,/bindMemoryButton\(context,"Manage"/,"Quick add needs a visible Manage control");
assert.match(grocery,/bindMemoryButton\(context,"Add item"/,"manager must allow manual reusable-item creation");
assert.match(grocery,/action:"edit"/,"manager must edit local Quick add metadata");
assert.match(grocery,/action:"pin"/,"manager must pin and unpin entries");
assert.match(grocery,/action:"hide"/,"manager must hide suggestions instead of deleting task history");
assert.match(grocery,/action:"restore"/,"manager must restore hidden suggestions");
assert.match(grocery,/action:"delete"/,"manager must delete a deliberately hidden suggestion");
assert.match(grocery,/Delete “\$\{item\.title\}” from hidden suggestions\?/,"hidden delete must ask for a touch-safe confirmation");
assert.match(grocery,/A future completed Grocery item with this name may be learned again\./,"hidden delete must explain the difference from Hide");
assert.match(grocery,/bindMemoryButton\(context,"Delete","grocery-memory-action grocery-memory-delete"/,"hidden rows must expose a dedicated Delete control");
assert.match(grocery,/Grocery items already on your list are unchanged/,"hide confirmation must explain its limited scope");
assert.ok(!/\b(?:window\.)?confirm\(/.test(grocery),"Quick add manager must use themed in-overlay confirmation");
assert.match(http,/\/api\/todo\/grocery-memory/,"local Quick add endpoint must be explicit");
assert.match(http,/case "delete":[\s\S]*?deleteTodoGroceryMemoryItem/,"Quick add deletion must remain a catalog-only server action");
assert.doesNotMatch(http,/enqueueTodoOp|todoStartDrain|todoGraph/,"Quick add catalog endpoint must not enqueue Graph work");
assert.match(memory,/Aliases\s+\[\]string/,"renamed suggestions must retain learned aliases");
assert.match(memory,/Hidden\s+bool/,"removed suggestions must be reversible hidden records");
assert.match(memory,/func \(a \*Service\) deleteTodoGroceryMemoryItem/,"hidden suggestion deletion needs a dedicated durable mutation");
assert.match(memory,/if !items\[i\]\.Hidden \{[\s\S]*?hide the quick add item before deleting it/,"only hidden suggestions may be permanently deleted");
assert.match(memory,/todoGroceryMemoryLimit\s*=\s*96/,"Grocery Memory must remain bounded");
assert.match(memory,/if items\[i\]\.Hidden \{\n\s*continue/,"hidden suggestions must not be re-learned automatically");
assert.match(memory,/a\.mutateSettings\(func\(settings map\[string\]any\) error/,"catalog changes must use the Settings mutation seam");
assert.match(settingsService,/s\.writeMu\.Lock\(\)/,"Settings service must serialize catalog mutations with the shared write lock");
assert.match(css,/\.grocery-memory-row/,"manager rows must have dedicated themed layout");
assert.match(css,/\.grocery-memory-actions/,"manager actions need touch-safe grouping");
assert.match(css,/\.grocery-memory-delete/,"hidden Delete controls need the established destructive action styling");
assert.match(launcher,/lists-grocery\.js/,"Lists loader must fetch the split Grocery module lazily");
assert.match(launcher,/appendListsScript\("ui\/lists-core\.js/,"core Lists script must load before Grocery management helpers");
console.log("PASS: Grocery Quick add management contract");
