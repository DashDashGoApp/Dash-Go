import assert from "node:assert/strict";
import { readFileSync } from "node:fs";
import { resolve } from "node:path";

const root=resolve(process.argv[2]||".");
const read=rel=>readFileSync(resolve(root,rel),"utf8");
const todoTypes=read("internal/todo/todo_types.go");
const todoPeople=read("internal/todo/todo_people.go");
const todoHTTP=read("cmd/dashboard-control-server/todo_http_tasks.go");
const graphPending=read("internal/todo/todo_graph_pending.go");
const graphLists=read("internal/todo/todo_graph_lists.go");
const launcher=read("ui/js/app-launcher.js");
const listsCore=read("ui/lists-core.js");
const listsPeople=read("ui/lists-people.js");
const maintenance=read("ui/maintenance.js");
const dock=read("ui/js/lists-dock.js");
const maintenanceHTTP=read("cmd/dashboard-control-server/maintenance_http.go");
const maintenanceModel=read("internal/household/maintenance/model.go");
const maintenanceCalendar=read("internal/household/maintenance/calendar.go");

assert.match(todoTypes,/DashGoAssignment\s+\*todoTaskAssignment/,"To Do cache must carry local responsibility metadata");
assert.match(todoPeople,/func \(a \*Service\) todoAssignTaskPerson/,"To Do must have a dedicated local-only assignment mutation");
assert.doesNotMatch(todoPeople,/enqueueTodoOp\s*\(/,"person assignment may not enqueue Microsoft Graph work");
assert.match(todoHTTP,/parts\[6\] == "assignment"/,"To Do must expose a narrow task-assignment endpoint");
assert.match(graphPending,/todoTaskGraphPatchBody\(op\.Payload\)/,"Graph PATCH must use the outbound allowlist");
assert.match(graphLists,/func todoTaskGraphPatchBody/,"Graph PATCH allowlist must be explicit");
assert.doesNotMatch(graphLists,/dashgoAssignment[^\n]*out\[/,"Dash-Go responsibility must not appear in outbound Graph patch bodies");
assert.match(launcher,/lists-people\.js/,"Lists must lazy-load the People UI with the app");
assert.match(listsCore,/personFilter:"all"/,"Lists must retain a local responsibility filter state");
assert.match(listsPeople,/\/assignment/,"Lists responsibility picker must call the dedicated local endpoint");
assert.match(listsPeople,/function usesMicrosoftTaskSync\(\)/,"Lists must identify an actually Microsoft-mapped active list before showing a cloud-compatibility note");
assert.match(listsPeople,/if\(usesMicrosoftTaskSync\(\)\)panel\.appendChild\(detail\);/,"Local-only Lists responsibility pickers must omit the unnecessary Microsoft warning");
assert.doesNotMatch(listsPeople,/\b(?:window\.)?confirm\s*\(/,"Lists must use a themed in-overlay person picker");
assert.match(dock,/task\.assignee/,"Bottom Lists dock must surface local task responsibility when present");
assert.match(maintenance,/Responsible/,"Maintenance form and rows must expose responsibility");
assert.match(maintenanceHTTP,/activePeople/,"Maintenance route adapter must snapshot the canonical active roster before the child lock");
assert.match(maintenanceModel,/responsiblePersonNameSnapshot/,"Maintenance history must retain a person-name snapshot");
assert.match(maintenanceCalendar,/Responsible:/,"Maintenance calendar descriptions must retain responsibility without polluting titles");
