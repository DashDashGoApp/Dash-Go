import assert from "node:assert/strict";
import {readFileSync} from "node:fs";
import {resolve} from "node:path";

const root=resolve(process.argv[2]||".");
const read=rel=>readFileSync(resolve(root,rel),"utf8");
const model=read("internal/calendar/household_schedules.go");
const generate=read("internal/calendar/household_schedules_generate.go");
const defaults=read("internal/calendar/defaults.go");
const pickup=read("internal/calendar/pickup.go");
const iss=read("internal/calendar/iss.go");
const cache=read("internal/calendar/events/cache.go");
const eventTypes=read("internal/calendar/events/types.go");
const popup=read("ui/js/managed-schedules-popup.js");
const parser=read("ui/js/ics-parser.js");
const eventPopup=read("ui/js/event-popup.js");
const control=read("ui/js/control-household-schedules.js");
const navigation=read("ui/js/control-navigation.js");
const index=read("index.html");
const jsManifest=read("ui/js/bundle.manifest.json");
const cssManifest=read("ui/css/bundle.manifest.json");

assert.match(model,/type HouseholdSchedules struct/,"household schedules need a versioned local model");
assert.match(model,/func legacyHouseholdSchedules/,"existing installer schedule values need a migration path");
assert.match(model,/func \(s \*Service\) SetHouseholdScheduleOverride/,"one-off moves and skips need a canonical persistence path");
assert.match(model,/SetHouseholdScheduleOverrideWithResult/,"one-off moves must return a resolved safety outcome");
assert.match(model,/no more than 90 days/,"manual move dates must be bounded near their occurrence");
assert.match(model,/dropped orphaned one-time schedule adjustments/,"stale orphaned overrides must not block schedule loading");
assert.match(model,/func \(s \*Service\) AvailableHolidayLayers/,"only installed holiday layers may be offered");
assert.match(model,/householdSchedulesFile/,"household schedules must use an explicit local config file");
assert.match(generate,/X-DASHGO-MANAGED-SCHEDULE/,"generated managed events need durable explicit ownership metadata");
assert.match(generate,/scheduleHolidayDates\(rule\.Adjustment, holidaysByLayer\)/,"each rule must use only its own selected holiday layers");
assert.match(generate,/override.Action == "skip"/,"one-off skips must suppress only the matched occurrence");
assert.match(generate,/monthly-dates/,"multiple monthly payday dates must be generated");
assert.match(generate,/seen\[key\]/,"month-end-clamped payday dates must be deduplicated");
assert.match(generate,/no safe pickup day found/,"holiday shifts must not land silently on a second holiday");
assert.match(defaults,/writeHouseholdSchedulesLocked/,"startup generation must persist the legacy migration once");
assert.match(defaults,/SeasonEvents\(years\)/,"season dates must be calculated instead of fixed");
assert.match(defaults,/moon\.violet\.ics/,"only the actual moon output may be reported as written");
assert.match(pickup,/Observed February 28/,"February 29 celebrations need an explicit non-leap-year policy");
assert.match(pickup,/celebrationUID/,"celebration UIDs must not depend on file line order");
assert.match(iss,/issEnvelopePresent/,"ISS updates must validate the provider response envelope");
assert.match(iss,/lat\/lon are not configured/,"ISS must reject the 0,0 placeholder location");
assert.match(cache,/managedSchedule/,"event cache must expose managed schedule metadata to the dashboard");
assert.match(eventTypes,/const CacheVersion = 6/,"managed schedule metadata must invalidate earlier event caches");
assert.match(popup,/function managedScheduleInfo/,"day popup must validate explicit schedule metadata");
assert.match(popup,/managedScheduleShiftGrid/,"day popup must offer quick date offsets");
assert.match(popup,/adjustment&&adjustment\.collision/,"day popup must surface same-rule move collisions");
assert.match(popup,/within 90 days/,"day popup must explain manual move bounds");
assert.match(popup,/Edit recurring schedule/,"day popup must link one-off corrections to recurring edits");
assert.doesNotMatch(popup,/title.*(?:payday|trash|recycl)/i,"popup must not infer schedule ownership from visible event titles");
assert.match(eventPopup,/showManagedSchedulePopup\(ev\)/,"managed events must route through the schedule correction popup");
assert.match(parser,/X-DASHGO-MANAGED-SCHEDULE/,"direct ICS fallback must retain managed schedule metadata");
assert.match(control,/function renderCtrlHouseholdSchedules/,"Dashboard Control needs a lazy household schedule renderer");
assert.match(control,/Monthly date\(s\)/,"Payday editor must offer multiple monthly dates");
assert.match(control,/Previous business day/,"Payday editor must offer business-day correction");
assert.match(control,/One-time adjustments/,"Control must let a user restore a moved or skipped occurrence");
assert.match(navigation,/case "schedules": await renderCtrlHouseholdSchedules\(\)/,"calendar lazy navigation must load household schedules");
assert.match(index,/data-lazy="schedules"[\s\S]*?Household Schedules/,"Calendar Control needs a Household Schedules section");
assert.match(jsManifest,/"control-household-schedules\.js"/,"control bundle must include the schedule editor");
assert.match(jsManifest,/"managed-schedules-popup\.js"/,"dashboard bundle must include the schedule popup");
assert.match(cssManifest,/"control\/household-schedules\.css"/,"control stylesheet must include schedule editor styles");
console.log("PASS: household schedule migration, explicit metadata, holiday-layer isolation, one-off corrections, and Control/day-popup paths are present");
