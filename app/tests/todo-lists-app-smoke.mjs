import assert from "node:assert/strict";
import fs from "node:fs";
const root=new URL("..",import.meta.url).pathname;
const lists=fs.readFileSync(root+"ui/lists-core.js","utf8")+"\n"+fs.readFileSync(root+"ui/lists-actions.js","utf8");
const index=fs.readFileSync(root+"index.html","utf8");
const listsCSS=fs.readFileSync(root+"ui/lists.css","utf8");
const grocery=fs.readFileSync(root+"ui/lists-grocery.js","utf8");
const launcher=fs.readFileSync(root+"ui/js/app-launcher.js","utf8");
const server=fs.readFileSync(root+"cmd/dashboard-control-server/todo_http.go","utf8");
const store=fs.readFileSync(root+"internal/todo/todo_store.go","utf8");
const control=fs.readFileSync(root+"ui/js/control-todo.js","utf8");
assert.match(launcher,/id:"todo"/,"To Do tile must remain registered");
assert.match(launcher,/id:"grocery"/,"Grocery tile must remain registered");
assert.ok(!launcher.includes("todoSlotAvailable"),"launcher may not hide To Do/Grocery based on mappings");
assert.ok(!launcher.includes("warmListsAssets"),"Lists must remain lazy until opened");
assert.ok(!server.includes('"/api/todo/apps"'),"removed visibility endpoint must not be routable");
assert.ok(!store.includes("todoAppsEnabled"),"server may not retain an app visibility switch");
assert.match(store,/out\[slot\] = defaults\[slot\]/,"empty legacy maps must fall back to local slots");
assert.match(control,/List destinations/,"Control must call mappings destinations, not launcher visibility");
assert.ok(!control.includes("Dashboard app visibility"),"Control must not expose app visibility buttons");
assert.ok(!control.includes("Show list apps"),"Control must not expose show apps action");
assert.ok(!control.includes("Hide list apps"),"Control must not expose hide apps action");
assert.ok(!control.includes("Not mapped"),"Control must not offer a mapping that hides an app");
assert.match(lists,/openListsImpl/,"Lists app must retain canonical local open lifecycle");
assert.match(lists,/closeLists/,"Lists app must retain close lifecycle");
assert.doesNotMatch(index,/id="listsapp-tabs"/,"Lists overlay must not expose To Do/Grocery switching tabs");
assert.doesNotMatch(lists,/function renderTabs\(/,"Lists overlay must not retain a cross-destination tab renderer");
assert.doesNotMatch(lists,/mappedSlots\(\)\[0\]/,"Grocery must never fall through to the To Do destination");
assert.match(lists,/function listIDForSlot\(slot\)/,"Lists must resolve only the launcher-selected destination");
assert.match(lists,/LISTS_STATE\.active=listIDForSlot\(LISTS_STATE\.slot\)/,"Lists must keep the requested To Do or Grocery destination active");
assert.match(lists,/setListsAppTitle\(listTitle\(listId\)\)/,"Lists header must identify the selected list without tabs");
assert.match(listsCSS,/grid-template-rows:auto minmax\(0,1fr\)/,"Lists shell must reclaim retired tab-row height");
assert.match(grocery,/renderQuickAdd/,"Grocery Quick add must remain a lazy Lists submodule");
assert.match(grocery,/renderManager/,"Grocery Quick add must have a focused manager view");
assert.doesNotMatch(listsCSS,/\.lists-tabs|\.lists-tab/,"Lists styling must not retain hidden tab controls");
assert.match(launcher,/id:"todo"[\s\S]*?launch:\(\)=>openListsApp\("todo"\)/,"To Do launcher tile must keep a direct destination");
assert.match(launcher,/id:"grocery"[\s\S]*?launch:\(\)=>openListsApp\("grocery"\)/,"Grocery launcher tile must keep a direct destination");
assert.match(launcher,/lists-grocery\.js/,"Grocery management helpers must load only with Lists");
console.log("PASS: permanent To Do/Grocery app contract");
