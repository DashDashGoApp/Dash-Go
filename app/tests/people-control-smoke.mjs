import assert from "node:assert/strict";
import {readFileSync} from "node:fs";
import {resolve} from "node:path";

const root=resolve(process.argv[2]||".");
const read=rel=>readFileSync(resolve(root,rel),"utf8");
const index=read("index.html");
const helper=read("ui/js/control-lazy-loader.js");
const nav=read("ui/js/control-navigation.js");
const control=read("ui/js/control-people.js");
const css=read("ui/css/control/people.css");
const consistency=read("ui/css/control/consistency.css");
const server=read("cmd/dashboard-control-server/household_people_control.go");
const impact=read("cmd/dashboard-control-server/household_people_impact.go");
const reconcile=read("cmd/dashboard-control-server/household_people_reconcile.go");
const getRoutes=read("cmd/dashboard-control-server/http_routes_get.go");
const postRoutes=read("cmd/dashboard-control-server/http_routes_post.go");
const inboxPins=read("cmd/dashboard-control-server/household_people_inbox_pins.go");
const routines=read("ui/routines.js");
const chores=read("ui/chore-wheel.js");
const lists=read("ui/lists-people.js");
const maintenance=read("ui/maintenance.js");
const launcher=read("ui/js/app-launcher.js");

assert.match(index,/data-lazy="people"[\s\S]*?data-people-control-root/,"Dashboard Control needs a lazy People card");
assert.match(nav,/case "people": await renderCtrlPeople\(\); break;/,"Control lazy navigation must render People");
assert.match(helper,/function openDashboardPeopleControl\(/,"apps need one shared contextual handoff helper");
assert.match(helper,/DASH_CONTROL_RETURN_ACTION/,"People handoff must return to its source app after Control closes");
assert.match(control,/\/api\/household\/people/,"People Control must use the canonical local endpoint");
assert.match(control,/Make future work unassigned/,"removing a person must offer an explicit unassignment choice");
assert.match(control,/Reassign future work to/,"removing a person must offer reassign choices");
assert.doesNotMatch(control,/\b(?:window\.)?confirm\s*\(/,"People Control must not use browser-native confirmations");
assert.match(control,/function peopleNameInput\(/,"People inputs must use one themed field helper");
assert.match(control,/const input=oskInput\(placeholder,value\);/,"People text entry must retain the shared OSK field class");
assert.doesNotMatch(control,/input\.className="people-text-input"/,"People must not override the shared OSK input class");
assert.match(control,/people-add-controls/,"Add-person controls must use the compact field/action layout");
assert.match(consistency,/#ctrl \.oskfield,[\s\S]*background:var\(--panel\)[\s\S]*color:var\(--fg\)[\s\S]*-webkit-text-fill-color:var\(--fg\)/,"People input must remain opaque and readable through the shared WebKit-safe Control field");
assert.match(consistency,/#ctrl \.oskfield::placeholder,\s*\.people-text-input::placeholder\{color:var\(--dimmer\);opacity:1;\}/,"People placeholder text must use theme-muted contrast through the shared field surface");
assert.match(css,/\.people-add-controls\{display:grid;grid-template-columns:minmax\(0,1fr\) auto/,"Wide People editor must keep name and add action in a tidy grid");
assert.match(css,/@media\(max-width:760px\)\{[\s\S]*\.people-add-controls\{grid-template-columns:1fr\}/,"Small screens must stack People field and action safely");
assert.match(css,/\.people-row\{/,"People card needs a dedicated compact row layout");
assert.match(server,/func \(a \*app\) handleHouseholdPeople(Post|Get)/,"server must expose the canonical People API");
assert.match(server,/householdPeopleDeleteTarget/,"person removal must validate its future-work resolution");
const peopleMutation=server.slice(server.indexOf("WithLock(func() error {"),server.indexOf("}); err"));
assert.ok(peopleMutation.length>0,"People mutation must retain one explicit roster-lock transaction");
assert.doesNotMatch(peopleMutation,/householdPeopleControlPayload\(/,"People response composition must happen after the roster lock is released");
const responseIndex=server.lastIndexOf("a.json(w, a.householdPeopleControlPayload())");
const rosterUnlockIndex=server.indexOf("}); err");
assert.ok(responseIndex>rosterUnlockIndex,"successful People responses must be built after mutation completion");
assert.match(impact,/householdPeopleImpactForRoster/,"People impact must accept an already-read roster projection");
assert.match(impact,/routinesPayloadForRoster\(/,"People impact must not seed/re-enter People through the normal Routines payload path");
assert.doesNotMatch(impact,/routinesPayload\(\)/,"People impact must not call the roster-seeding Routines payload helper");
assert.match(impact,/"routines".*"chores".*"maintenance".*"todo".*"grocery".*"messages"/,"People impact must cover every supported household consumer");
assert.match(reconcile,/reconcileHouseholdPeople(Routines|Chores|Maintenance|Todo)/,"person removal must reconcile each consumer deliberately");
assert.match(getRoutes,/handleHouseholdPeopleGet/,"People GET must remain behind the Control token boundary");
assert.match(postRoutes,/handleHouseholdPeoplePost/,"People mutations must remain behind the Control token boundary");
assert.match(postRoutes,/handleHouseholdPeopleInboxPINPost/,"Personal inbox PIN mutations must remain behind the Control token boundary");
assert.doesNotMatch(inboxPins,/set a Dashboard Control PIN/,"Inbox PIN management must not require a master Dashboard Control PIN");
assert.doesNotMatch(control,/masterPINEnabled/,"People UI must not hide individual PIN controls behind a master-PIN prerequisite");
assert.match(control,/function peopleNotificationCard\(/,"People Control must render per-person external notification preferences");
assert.match(control,/\/api\/household\/people\/notifications/,"People Control must persist only non-secret preference flags");
assert.doesNotMatch(control,/apprise:\/\/|discord:\/\/|https:\/\//,"People Control must not expose external delivery URLs");
assert.match(control,/This PIN protects this inbox on the shared display/,"People UI must explain optional local inbox PIN behavior");
for(const [name,source] of [["Routines",routines],["Chore Wheel",chores],["Lists",lists],["Maintenance",maintenance]]){
  assert.match(source,/openDashboardPeopleControl|Manage people/,`${name} must expose a contextual route to the canonical People editor`);
}
assert.doesNotMatch(routines,/\/api\/routines\/people/,"Routines must not retain a duplicate People mutation surface");
assert.doesNotMatch(launcher,/appId:\s*["']people["']|data-app=["']people["']/,"People must not consume an App Launcher tile");
console.log("People Control smoke: ok");
