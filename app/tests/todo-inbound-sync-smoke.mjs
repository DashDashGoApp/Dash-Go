import assert from "node:assert/strict";
import {readFileSync} from "node:fs";
import {resolve} from "node:path";

const root=resolve(process.argv[2]||".");
const read=rel=>readFileSync(resolve(root,rel),"utf8");
const lists=read("ui/lists-core.js")+"\n"+read("ui/lists-actions.js");
const dock=read("ui/js/lists-dock.js");
const control=read("ui/js/control-todo.js");
const inbound=read("internal/todo/todo_inbound.go")+"\n"+read("internal/todo/todo_inbound_coordinator.go")+"\n"+read("internal/todo/todo_inbound_results.go");
const candidates=read("internal/todo/todo_inbound_candidates.go");
const graph=read("internal/todo/todo_graph_delta.go");
const graphLists=read("internal/todo/todo_graph_lists.go");
const http=read("cmd/dashboard-control-server/todo_http.go");
const types=read("internal/todo/todo_types.go");

for(const [name,source] of [["Lists",lists],["Bottom Lists dock",dock]]){
  assert.match(source,/addEventListener\("todo",refresh\)/,`${name} must subscribe to named todo SSE events`);
  assert.match(source,/addEventListener\("sync\.state",(?:syncState|refresh)\)/,`${name} must subscribe to named sync-state SSE events`);
  assert.doesNotMatch(source,/\.onmessage\s*=/,`${name} may not rely on unnamed EventSource messages`);
}
assert.match(http,/eventName := "todo"[\s\S]*?eventName = "sync\.state"/,"server must emit sync-state as a named SSE event");
assert.match(types,/todoInboundSyncFixedSeconds\s*=\s*25/,"Automatic Microsoft sync must be fixed at 25 seconds");
assert.doesNotMatch(types,/todoInboundSyncFastSeconds|todoInboundSyncBalancedSeconds|todoInboundSyncLowPowerSeconds/,"Retired user-tunable Microsoft sync rates must not remain in runtime constants");
assert.match(inbound,/todoNormalizeInboundSyncSetting/,"legacy Manual/Fast/Balanced/Low-power settings must migrate to the fixed 25-second cadence");
assert.match(inbound,/todoInboundSyncFixedSeconds/,"scheduler must use the fixed inbound cadence");
assert.match(candidates,/todoMappedMicrosoftListIDs/,"server must retain explicit To Do/Grocery destination mapping");
assert.match(candidates,/todoPendingMicrosoftListIDs/,"scheduler must include a list with active Dash-Go queue work");
assert.match(candidates,/todoEstablishedCursorListIDs/,"scheduler must retain a previously opened Microsoft list with a saved delta cursor");
assert.match(candidates,/DeltaLink/,"tracked-list eligibility must be cursor-gated rather than discovery-wide");
assert.doesNotMatch(candidates,/todoAllCloudSyncListIDs/,"scheduler must not poll every discovered Microsoft list");
assert.match(candidates,/todoInboundMicrosoftListIDs/,"all inbound entry points must share the bounded candidate set");
assert.match(inbound,/todoInboundMicrosoftListIDs/,"scheduler and Sync now must use the shared inbound candidate set");
assert.match(inbound,/todoUniqueMicrosoftListIDs/,"server must deduplicate shared To Do/Grocery mappings");
assert.match(inbound,/todoCloudMu\.Lock\(\)/,"server must serialize outbound flushes and delta pulls");
assert.match(inbound,/todoPartialSyncError/,"a failed list must not back off a successful sibling list");
assert.match(inbound,/result\.OK = false/,"manual Sync now must return safe per-list diagnostics when every requested list fails");
assert.match(http,/"ok": result\.OK/,"Sync now HTTP response must preserve an unsuccessful but structured list result");
assert.match(inbound,/todoThrottleError/,"server must honor throttling via a structured error");
assert.match(inbound,/todoInboundSyncMaxBackoff/,"server must bound retry backoff");
assert.match(graph,/return "\/me\/todo\/lists\/" \+ todoGraphPathID\(listID\) \+ "\/tasks\/delta"/,"task pulls must start on the bare Microsoft To Do delta route");
assert.match(graphLists,/return "\/me\/todo\/lists\/delta"/,"list discovery must start on the bare Microsoft To Do list delta route");
assert.doesNotMatch(graph,/\/tasks\/delta\?\$select=/,"initial task delta must not send optional $select to a rejecting Graph request broker");
assert.doesNotMatch(graphLists,/\/lists\/delta\?\$select=/,"initial list delta must not send optional $select to a rejecting Graph request broker");
assert.match(graph,/@odata\.nextLink/,"delta sync must follow paginated Graph pages");
assert.match(graph,/@odata\.deltaLink/,"delta sync must persist a Graph continuation token");
assert.match(graph,/@removed/,"delta sync must apply remote deletions");
assert.match(graph,/PendingOps/,"delta merge must preserve unsettled local writes");
assert.match(graph,/todoClearStaleTaskPending/,"delta merge must clear stale pending flags that no longer have a queued operation");
assert.match(graph,/todoFinalDeltaRows/,"delta merge must coalesce repeated sparse Graph rows");
assert.match(graph,/todoGraphNeedsDeltaReset/,"delta sync must reset invalid Graph cursors");
assert.match(control,/Automatic Microsoft sync · every 25 seconds/,"Control must explain the fixed 25-second household cadence");
assert.match(control,/Opening To Do or Grocery requests an immediate background check/,"Control must explain the immediate list-open trigger");
assert.match(control,/Dash-Go list changes save locally, then send and reconcile afterward/,"Control must explain the immediate local-write trigger");
assert.match(control,/Sync Microsoft To Do now/,"Control must expose direct Microsoft task reconciliation");
assert.doesNotMatch(control,/Fast · every 15 seconds|Balanced · every 30 seconds|Manual · no automatic pull|Save sync cadence|Enable 30-second automatic pull/,"Control must not expose retired cadence tuning");
assert.match(control,/Microsoft task sync needs attention/,"Control must surface a failing inbound pull");
assert.match(control,/Refresh available Microsoft lists/,"Control must distinguish list-name refresh from task sync");
assert.doesNotMatch(control,/\/api\/todo\/inbound-sync/,"Current Control UI must not expose the retired cadence endpoint");
assert.match(control,/\/api\/todo\/sync/,"Control must invoke the mapped-task sync route");
assert.match(control,/sync-failures/,"Control must offer explicit recovery for blocked outbound writes");
console.log("PASS: Microsoft To Do inbound delta sync contract");
