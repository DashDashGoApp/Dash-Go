import assert from "node:assert/strict";
import {readFileSync} from "node:fs";
import {resolve} from "node:path";

const root=resolve(process.argv[2]||".");
const read=rel=>readFileSync(resolve(root,rel),"utf8");
const control=read("ui/js/control-calendars.js");
const css=read("ui/css/control/consistency.css");
const manifest=read("internal/calendar/manifest.go");
const manager=read("internal/calendar/management.go");
const trash=read("internal/calendar/trash.go");
const types=read("internal/calendar/types.go");
const facade=read("cmd/dashboard-control-server/calendar_facade.go");
const routesGet=read("cmd/dashboard-control-server/http_routes_get.go");
const routesPost=read("cmd/dashboard-control-server/http_routes_post.go");
const chore=read("ui/chore-wheel.js");
const choreCore=read("ui/chore-wheel-core.js");
const maintenance=read("ui/maintenance.js");

assert.match(routesGet,/case "\/api\/calendars\/manage":/,'Calendar Manager needs a read endpoint');
assert.match(routesPost,/\/api\/calendars\/manage\/delete[\s\S]*?\/api\/calendars\/manage\/restore[\s\S]*?\/api\/calendars\/manage\/app-output[\s\S]*?\/api\/calendars\/manage\/repair/,'Calendar Manager needs bounded mutation endpoints');
assert.match(control,/Manage calendars/,'Dashboard Control needs an explicit Calendar Manager entry point');
assert.match(control,/Repair calendar index/,'Dashboard Control needs a non-destructive repair action');
assert.match(control,/\/api\/calendars\/manage/,'manager data must load only through the management endpoint');
assert.match(control,/Move this \.ics file to Calendar Trash/,'local deletion must describe archival rather than permanent deletion');
assert.match(control,/Only the Dash-Go symlink is removed/,'symlink removal must promise external-target safety');
assert.match(control,/Stop calendar output/,'app rows must offer an output-specific action instead of a false delete');
assert.match(control,/confirmAction\(/,'destructive calendar actions must use in-app two-tap confirmation');
assert.doesNotMatch(control,/\b(?:window\.)?(?:alert|confirm|prompt)\s*\(/,'Calendar Manager may not use browser dialogs');
assert.match(css,/\.calmanager\{[\s\S]*?max-height:min\(58vh,640px\)[\s\S]*?overflow:auto/,'Calendar Manager needs one bounded scroll region');
assert.match(css,/\.calmanager-row/,'Calendar Manager rows need themed panel styling');
assert.match(types,/TrashRetentionDays\s*=\s*30/,'Calendar Trash retention must remain 30 days');
assert.match(manager,/os\.Rename\(path, destination\)/,'archive must move a local source rather than unlink it');
assert.match(trash,/os\.Remove\(filepath\.Join\(s\.TrashDir\(\), record\.TrashName\)\)/,'expired trash must be physically purged');
assert.match(manager,/os\.Lstat/,'calendar path safety must distinguish a symlink from its target');
assert.match(trash,/calendar source is not a managed local file/,'unknown/remote paths must be rejected server-side');
assert.match(manager,/OwnedOwner\(url\) != ""/,'app feeds must be protected from ordinary local deletion');
assert.match(manager,/func \(s \*Service\) SetOwnedOutput/,'app feeds must use an output setting rather than file deletion');
assert.match(manager,/func \(s \*Service\) Repair/,'repair must be a dedicated non-destructive operation');
assert.match(manifest,/remember := func/,'manifest repair must carry forward prior calendar preferences');
assert.match(manifest,/preserve the safer hidden intent/,'duplicate repair must never unexpectedly re-show a hidden source');
assert.match(manifest,/outputSnapshot\(\)/,'Calendar must snapshot household output before taking its own lock');
assert.match(facade,/calendarpkg\.New\(calendarpkg\.ServiceConfig\{/, 'core must construct the bounded Calendar service explicitly');
assert.match(facade,/calendarOutputSnapshot/, 'household commits must pass output snapshots to Calendar');
assert.match(choreCore,/calendarOutputEnabled:settings\.calendarOutputEnabled!==false/,'browser Chore Wheel model must preserve calendar output state');
assert.match(chore,/Enable Chores calendar/,'Chore Wheel must explain and recover from disabled output');
assert.match(maintenance,/Enable Maintenance calendar/,'Maintenance must explain and recover from disabled output');
assert.ok(!/setInterval/.test(control),'Calendar Manager must not add a polling loop');
console.log('calendar manager smoke: ownership-aware archive, restore, app output, visibility-safe repair, and bounded UI contracts hold');
