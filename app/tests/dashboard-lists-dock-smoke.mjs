import assert from "node:assert/strict";
import { readFileSync } from "node:fs";
import { resolve } from "node:path";

const root=resolve(process.argv[2]||".");
const read=rel=>readFileSync(resolve(root,rel),"utf8");
const index=read("index.html");
const css=read("ui/css/dashboard/lists-dock.css");
const dock=read("ui/js/lists-dock.js");
const launcher=read("ui/js/app-launcher.js");
const control=read("ui/js/control-todo.js");
const lists=read("ui/lists-core.js")+"\n"+read("ui/lists-actions.js");
const server=read("cmd/dashboard-control-server/todo_http.go");
const store=read("internal/todo/todo_store.go");

assert.match(index,/id="listsdock"[^>]*aria-label="Dashboard Lists dock"[^>]*hidden/,"dashboard must provide a static, hidden Lists dock shell");
assert.match(css,/#app\.lists-dock-visible\{[\s\S]*?grid-template-areas:[\s\S]*?"listsdock\s+listsdock"/,"dock must be an in-flow dashboard grid row");
assert.match(css,/#app\.lists-dock-visible #calwrap,[\s\S]*?#app\.lists-dock-visible #sidebar,[\s\S]*?min-height:0/,"dock layout must permit calendar/sidebar shrinkage");
assert.match(css,/\.listsdock-body\{[\s\S]*?overflow-y:auto/,"expanded dock body must be its own scroll region");
assert.doesNotMatch(css,/translateY\s*\(/,"dock may not shift the dashboard with a transform");
assert.match(dock,/dashboardListsDockSettingEnabled[\s\S]*?dashboardDock===true/,"dock must default off unless explicitly persisted");
assert.match(dock,/dashboardListsDockSetVisible\(dashboardListsDockHasOpenItems\(\)\)/,"enabled-but-empty dock must reclaim the dashboard row");
assert.match(dock,/new EventSource\("\/api\/todo\/stream"/,"armed empty dock must retain the bounded local Lists stream for automatic reappearance");
assert.match(dock,/fetch\("\/api\/todo\/dock"/,"dock must use the bounded cache-only summary endpoint");
assert.doesNotMatch(dock,/function dashboardListsDockLoadTasks\(/,"dashboard ticker must not retain the former per-list load path");
assert.match(dock,/DASH_LIST_DOCK_TICKER_MAX_ITEMS=12/,"ticker source must be bounded");
assert.match(dock,/dashboardListsDockBuildTicker/,"dock must render a dedicated compact ticker");
assert.match(dock,/dashboardListsDockInboundSyncError/,"dock must expose an inbound sync issue without polling Graph");
assert.match(dock,/dashboardListsDockFetchStatus\(\)/,"sync-state events must refresh the local inbound status");
assert.match(dock,/Sync issue/,"dock must render a compact sync-error affordance");
assert.match(dock,/prefers-reduced-motion/,"ticker measurement must respect reduced motion");
assert.match(css,/@keyframes listsdock-ticker-crawl/,"ticker must use transform-only crawl animation");
assert.match(css,/@media\(prefers-reduced-motion:reduce\)/,"ticker CSS must disable motion when requested");
assert.match(control,/Bottom Lists dock/,"Dashboard Control must expose the dock preference");
assert.match(control,/Lists shown in Bottom Lists dock/,"Dashboard Control must expose per-list ticker selection");
assert.match(control,/Choose at least one list/,"Control must prevent an unusable all-hidden selection");
assert.match(control,/\/api\/todo\/dock\/slots/,"Control selection must use the dedicated persisted endpoint");
assert.match(lists,/window\.openListsForAdd=openListsForAdd/,"Lists app must expose the shared add prompt path");
assert.match(launcher,/window\.openListsApp\s*=\s*openListsApp/,"Lists launcher must expose the normal dock opening path");
assert.match(launcher,/window\.openListsAppWithAdd\s*=\s*openListsAppWithAdd/,"Lists launcher must expose the dock quick-add opening path");
assert.match(dock,/const open=add\?window\.openListsAppWithAdd:window\.openListsApp/,"dock must use the two exported Lists opening paths");
assert.match(dock,/taskError:""/,"dock must retain a bounded visible task-mutation error state");
assert.match(dock,/Could not update \$\{dashboardListsDockText\(task\.title\)\}\. Try again\./,"dock mutation failures must be visible instead of console-only");
assert.match(dock,/listsdock-task-status/,"dock must render task-mutation feedback in the expanded panel");
assert.match(css,/\.listsdock-task-status/,"dock mutation feedback needs dedicated themed layout");
assert.match(server,/case "\/api\/todo\/dock":/,"server must expose a cache-only dock summary endpoint");
assert.match(server,/case "\/api\/todo\/dock\/slots":/,"server must persist selected dock lists through the To Do settings boundary");
assert.match(store,/func \(a \*Service\) todoDashboardDockSlots\(\) map\[string\]bool/,"server must default legacy docks to both selected lists");
assert.match(store,/func \(a \*Service\) todoDashboardDockSummary\(\) todoDashboardDockSummary/,"server must provide a bounded local-cache summary");
console.log("dashboard Lists dock smoke: ok");
