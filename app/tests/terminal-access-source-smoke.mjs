#!/usr/bin/env node
import assert from "node:assert/strict";
import fs from "node:fs";
import path from "node:path";
import {fileURLToPath} from "node:url";

const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),"..");
const read=rel=>fs.readFileSync(path.join(root,rel),"utf8");
const access=read("internal/platform/service.go")+read("internal/platform/terminal.go");
const manager=access;
const post=read("cmd/dashboard-control-server/http_routes_post.go");
const status=read("internal/platform/system.go");
const main=read("cmd/dashboard-control-server/main.go");
const helper=read("bin/dashboard-terminal-access");
const index=read("index.html");
const terminalUi=read("ui/js/control-terminal-profile.js");
const navigation=read("ui/js/control-navigation.js");
const health=read("ui/js/control-status-health.js");
const backup=read("cmd/dashboard-control-server/config_backups.go");
const restore=read("cmd/dashboard-control-server/config_backup_restore.go");
const repositoryRoot=path.resolve(root,"..");
const readme=fs.readFileSync(path.join(repositoryRoot,"README.md"),"utf8");
const installer=fs.readFileSync(path.join(repositoryRoot,"installer","install.sh"),"utf8");

assert.match(access,/\.dashboard-terminal-access\.env/,"terminal switch must be a private home-side setting");
assert.match(access,/malformed state remains enabled/,"existing installations must keep terminal access enabled by default");
assert.match(access,/fileio\.WriteAtomic\(s\.TerminalAccessFile\(\), \[\]byte\(value\), 0600\)/,"terminal switch must be atomically owner-only");
assert.match(access,/usage: dashboard-terminal-access status\|enable\|disable/,"terminal switch CLI must expose only SSH management verbs");
assert.match(main,/case "--terminal-access":\s*os\.Exit\(a\.runTerminalAccessCLI\(os\.Args\[2:\]\)\)/,"server CLI must wire terminal-access helper command");
assert.match(helper,/dashboard-control-server" --terminal-access/,"shell helper must delegate to the validated server CLI");
assert.match(manager,/if !s\.TerminalAccessEnabled\(\) \{[\s\S]*?"enabled": false/,"terminal status must expose disabled state without probing terminal dependencies");
assert.match(manager,/return nil, ErrTerminalAccessDisabled/,"terminal launch must deny disabled state at the server boundary");
assert.match(post,/errors\.Is\(err, errTerminalAccessDisabled\)[\s\S]*?http\.StatusForbidden/,"direct terminal launch API must return forbidden when disabled");
assert.match(status,/"terminalAccessEnabled": s\.TerminalAccessEnabled\(\)/,"Control status must carry terminal visibility state");
assert.match(index,/ctrlsec-terminal[^>]*data-lazy="terminal"/,"Terminal access must remain a discrete Control card when enabled");
assert.match(terminalUi,/section\.hidden=!available/,"disabled terminal access must omit the full card rather than only disable a button");
assert.match(terminalUi,/if\(!syncTerminalAccessCard\(st&&st\.enabled!==false\)\)\{wrap\.innerHTML="";return;\}/,"Terminal renderer must not paint hidden-card content");
assert.match(navigation,/name==="system"&&typeof refreshTerminalAccessCard===/,"System page must refresh card visibility even when Status was never opened");
assert.match(health,/syncTerminalAccessCard\(!st\|\|st\.terminalAccessEnabled!==false\)/,"Status response must reconcile terminal card visibility");
assert.match(backup,/secrets\/terminal-access\.env/,"config backups must preserve SSH terminal-access selection");
assert.match(restore,/key != "secrets\/terminal-access\.env"/,"restore must allowlist only the known terminal secret");
assert.match(restore,/func \(a \*app\) restoreTerminalAccess\(stage string\) error/,"restore must validate and restore terminal-access selection");
assert.match(restore,/restore terminal access setting failed/,"restore errors must remain fail-closed");
assert.match(readme,/dashboard-terminal-access (?:status|`status`)/,"README must provide the direct SSH management command");
assert.match(readme,/choose \*\*Terminal access\*\*/,"README must document the installer toggle by name");
assert.match(installer,/configure_terminal_access\(\)\{[\s\S]*?release_server_for_host "\$DASH"[\s\S]*?--terminal-access status[\s\S]*?--terminal-access "\$next"/,"installer toggle must query and then atomically flip terminal access through the host-matched server");
assert.match(installer,/OPT_TERMINAL=24/,"installer must retain a stable terminal-access menu identity");
assert.match(installer,/\$\{OPT_TERMINAL\}\) Terminal access\s+toggle Dashboard Control Terminal card/,"installer must expose the terminal-access toggle");
assert.match(installer,/"\$OPT_TERMINAL"\) configure_terminal_access; exit \$\?;;/,"installer terminal-access choice must dispatch directly and exit");
console.log("PASS: terminal access remains private, owner-only, browser-denied when disabled, and platform-service owned");
